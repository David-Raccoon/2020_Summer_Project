package main.domian;

public class Favor {

	private int favorId;

	private int uid;

	private int imageId;

	public int getFavorId() {
		return favorId;
	}

	public void setFavorId(int favorId) {
		this.favorId = favorId;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getImageId() {
		return imageId;
	}

	public void setImageId(int imageId) {
		this.imageId = imageId;
	}

}
